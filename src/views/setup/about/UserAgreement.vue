export default {
        components: {
                Title
        },
        data() {
                return {
                        time: null,
                        style: {},
                        titlecen: 'User Agreement',
                };
        },
        created() {
                let asd = require('../../../JSON/public.json')
                this.style = asd
                this.time = new Date().getTime();
        },
        mounted() {
                setTitleBar("User Agreement");
                let totalTmie = new Date().getTime()
                this.$store.dispatch("onStatistics", {
                        type: "load",
                        event: "HP1602000",
                        cost: this.time,
                        totalCost: totalTmie,
                });

                window.addEventListener('scroll', this.handleScroll, true);
        },
        methods: {
                //滚动事件
                handleScroll() {
                        var scrollTop = window.pageYOffset || document.documentElement.scrollTop || document.body.scrollTop;
                        var offsetTop = document.querySelector('#tabbarBox').offsetTop // 获取所需元素到文档顶部的距离，然后判断滚动条位置让该元素是否固定定位
                        console.log(scrollTop, offsetTop)
                        if (scrollTop > offsetTop) {
                                this.tabbarBoxFixed = true
                        } else {
                                this.tabbarBoxFixed = false
                        }
                },
        },
        destroyed() { //离开这个界面之后，删除滚动事件，不然容易除bug
                window.removeEventListener('scroll', this.handleScroll)
        },
};
</script>
